class UserData {
  final String email;
  final String fullName;
  final int age;
  final double weight;
  final double height;
  final String gender;
  final String profilePictureUrl;

  UserData({
    required this.email,
    required this.fullName,
    required this.age,
    required this.weight,
    required this.height,
    required this.gender,
    required this.profilePictureUrl,
  });
}
